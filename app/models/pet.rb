class Pet < ApplicationRecord
  belongs_to :user
  has_many :appointments
  has_many :vets, through: :appointments

  def index
    @pets = Pet.all
  end

  def new
    @pet = Pet.new
  end

  def create
    @pet = Pet.new(pet_params)
    @pet.user = current_user
    if @pet.save
      redirect_to pet_path(@pet)
    else
      render :new
    end
  end

end
